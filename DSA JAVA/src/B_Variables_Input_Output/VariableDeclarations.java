package B_Variables_Input_Output;

public class VariableDeclarations {
    public static void main(String[] args) {
        // Variable declarations
        int age = 25;                        // Age of a person
        short temperature = -10;             // Temperature in Celsius
        long population = 789_000_000L;      // Population of a country
        float height = 1.75f;                // Height of a person in meters
        double distance = 4567.89123;        // Distance in kilometers
        char grade = 'A';                    // Grade of a student
        boolean isValid = true;              // Validation flag

        // Displaying the values
        System.out.println("Age: " + age);
        System.out.println("Temperature: " + temperature);
        System.out.println("Population: " + population);
        System.out.println("Height: " + height);
        System.out.println("Distance: " + distance);
        System.out.println("Grade: " + grade);
        System.out.println("isValid: " + isValid);


    }
}
